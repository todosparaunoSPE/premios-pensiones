# Proceso de evaluación
st.markdown("""
### Proceso de Evaluación
1. **Convocatoria:** Se abre una convocatoria pública invitando a las entidades de la industria de pensiones a postularse en las diferentes categorías.
2. **Recepción de Candidaturas:** Las organizaciones interesadas presentan sus candidaturas con la documentación y evidencias requeridas.
3. **Comité de Evaluación:** Un comité compuesto por expertos en el sector financiero y de pensiones evaluará las candidaturas basándose en los criterios establecidos.
4. **Selección de Finalistas:** Se seleccionan los finalistas para cada categoría.
5. **Ceremonia de Premiación:** Se realiza una ceremonia para anunciar y celebrar a los ganadores.
""")

